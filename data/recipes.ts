export function getRecipeById(id: string): Recipe | undefined {
  return recipes.find(r => r.id === id);
}

export function getRecipesByMode(mode: string): Recipe[] {
  return recipes.filter(r => r.mode === mode);
}

export function getRecipesByMealType(mealType: string): Recipe[] {
  return recipes.filter(r => r.mealTypes.includes(mealType as never));
}

export function getRecipesByCuisine(cuisine: string): Recipe[] {
  return recipes.filter(r => r.cuisine === cuisine);
}

export function getRecipesByBudget(maxCost: number): Recipe[] {
  return recipes.filter(r => r.costEstimate <= maxCost);
}

export function searchRecipes(query: string): Recipe[] {
  const q = query.toLowerCase();
  return recipes.filter(r =>
    r.title.toLowerCase().includes(q) ||
    r.tags.some(t => t.includes(q)) ||
    r.cuisine.toLowerCase().includes(q) ||
    r.ingredients.some(i => i.toLowerCase().includes(q))
  );
}
